//获得ADC值
//ch: 通道值 0~16，取值范围为：ADC_CHANNEL_0~ADC_CHANNEL_16
//返回值:转换结果
static uint16_t Get_Adc(uint32_t ch,uint8_t rank)   
{
    ADC_ChannelConfTypeDef ADC1_Handler={0};
   // ADC_ChannelConfTypeDef sConfig = {0};
    ADC1_Handler.Channel=ch;                                   //通道
    ADC1_Handler.Rank=rank;                                       //第1个序列，序列1
    ADC1_Handler.SamplingTime= ADC_SAMPLETIME_79CYCLES_5;      //采样时间               
    HAL_ADC_ConfigChannel(&hadc1,&ADC1_Handler);        //通道配置
	
	
    HAL_ADC_Start(&hadc1);                               //开启ADC
	
    HAL_ADC_PollForConversion(&hadc1,10);                //轮询转换
 
	return (uint16_t)HAL_ADC_GetValue(&hadc1);	        	//返回最近一次ADC1规则组的转换结果
}
  //获取指定通道的转换值，取times次,然后平均 
//times:获取次数
//返回值:通道ch的times次转换结果平均值
uint16_t Get_Adc_Average(uint32_t ch,uint8_t times,uint8_t rank)
{
	uint32_t temp_val=0;
	uint8_t t;
	for(t=0;t<times;t++)
	{
		temp_val+=Get_Adc(ch,rank);
		HAL_Delay(5);
	}
	return temp_val/times;
} 
